<template>
<div>
  <div>
    <el-date-picker
      v-model="thedate"
      type="date"
      placeholder="选择日期"
      @change="getData">
    </el-date-picker>
  </div>
    <el-table
      :data="stocks"
      style="width:60%"
      border
      :show-header="true">
      <el-table-column
        type="index">
      </el-table-column>
      <el-table-column
        prop="name"
        label="名称">
      </el-table-column>
      <el-table-column
        prop="okYears"
        label="入选年份">
      </el-table-column>
      <el-table-column
        prop="upProbability"
        label="上涨概率">
      </el-table-column>

    </el-table>
  </div>
</template>

<script>
export default {
  name: 'Bluechips',
  data () {
    return {
      stocks:[],
      thedate:''
    }
  },
  mounted: function() {
    this.getData();
  },
  methods:{
    getData:function(){      
      var vm = this;
      var apiurl = process.env.API_ROOT + 'bluechips';
      this.$http.get(apiurl,{params:{'date':vm.thedate}})
              .then(function(response){
                vm.stocks = response.data.content;
                //console.log(vm.questions);
             })
              .catch(function(response) {
                console.log("getData: there are something wrong!!!");
                console.log(apiurl)
                console.log(response);
              })

    }
  }



}
</script>

